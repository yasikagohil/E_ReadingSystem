package entity;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Akshay
 */
@Entity
@Table(name = "content_category")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "ContentCategory.findAll", query = "SELECT c FROM ContentCategory c"),
    @NamedQuery(name = "ContentCategory.findByCategoryId", query = "SELECT c FROM ContentCategory c WHERE c.categoryId = :categoryId"),
    @NamedQuery(name = "ContentCategory.findByCategoryName", query = "SELECT c FROM ContentCategory c WHERE c.categoryName = :categoryName")})
public class ContentCategory implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "categoryId")
    private Integer categoryId;
    @Basic(optional = false)
    @Column(name = "categoryName")
    private String categoryName;

    public ContentCategory() {
    }

    public ContentCategory(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public ContentCategory(Integer categoryId, String categoryName) {
        this.categoryId = categoryId;
        this.categoryName = categoryName;
    }

    public Integer getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (categoryId != null ? categoryId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ContentCategory)) {
            return false;
        }
        ContentCategory other = (ContentCategory) object;
        if ((this.categoryId == null && other.categoryId != null) || (this.categoryId != null && !this.categoryId.equals(other.categoryId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entity.ContentCategory[ categoryId=" + categoryId + " ]";
    }
    
}
